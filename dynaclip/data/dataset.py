"""
DynaCLIP Dataset & DataLoader: Contrastive pairs with dynamics similarity labels.

Supports hard-negative mining, DINOv2 embedding pre-computation, and the
Invisible Physics test split. Uses REAL images from DomainNet/COCO.
"""

import json
import logging
from pathlib import Path
from typing import Optional

import numpy as np
import torch
from torch.utils.data import Dataset, DataLoader, DistributedSampler
import torchvision.transforms as T

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Image transforms
# ---------------------------------------------------------------------------
def get_train_transform(img_size: int = 224) -> T.Compose:
    return T.Compose([
        T.RandomResizedCrop(img_size, scale=(0.8, 1.0)),
        T.RandomHorizontalFlip(p=0.5),
        T.ColorJitter(brightness=0.1, contrast=0.1, saturation=0.1, hue=0.05),
        T.ToTensor(),
        T.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])


def get_eval_transform(img_size: int = 224) -> T.Compose:
    return T.Compose([
        T.Resize(img_size),
        T.CenterCrop(img_size),
        T.ToTensor(),
        T.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])


# ---------------------------------------------------------------------------
# Main training dataset: contrastive pairs with dynamics similarity
# ---------------------------------------------------------------------------
class DynaCLIPContrastiveDataset(Dataset):
    """Dataset returning pairs (img_i, img_j, dynamics_similarity).

    Pair mining:
      - 30% hard negatives: same image but different physics (high visual sim, varying dyn sim)
      - 30% hard positives: different image but similar physics
      - 40% random
    """

    def __init__(
        self,
        data_dir: str,
        split: str = "train",
        transform: Optional[T.Compose] = None,
        num_pairs: int = 200_000,
        hard_neg_ratio: float = 0.3,
        hard_pos_ratio: float = 0.3,
        dino_embeddings_path: Optional[str] = None,
        similarity_path: Optional[str] = None,
        seed: int = 42,
    ):
        super().__init__()
        self.data_dir = Path(data_dir)
        self.split = split
        self.transform = transform or get_train_transform()
        self.num_pairs = num_pairs
        self.hard_neg_ratio = hard_neg_ratio
        self.hard_pos_ratio = hard_pos_ratio
        self.rng = np.random.default_rng(seed)

        # Load metadata - MUST exist (generated by generation.py)
        meta_path = self.data_dir / "metadata.json"
        if not meta_path.exists():
            raise FileNotFoundError(
                f"metadata.json not found at {meta_path}. "
                "Run data generation first: python -m dynaclip.data.generation"
            )
        with open(meta_path) as f:
            self.metadata = json.load(f)
        logger.info(f"Loaded {len(self.metadata)} entries from {meta_path}")

        # Build image_group index for hard mining
        self._group_index = {}
        for i, entry in enumerate(self.metadata):
            grp = entry.get("image_group", i)
            self._group_index.setdefault(grp, []).append(i)

        # Load dynamics similarity matrix (optional, from generation.py)
        self.sim_lookup = {}
        sim_path = similarity_path or str(self.data_dir / "similarity_matrix.npz")
        if Path(sim_path).exists():
            data = np.load(sim_path)
            pairs_arr = data["pairs"]
            sims_arr = data["similarities"]
            for k in range(len(pairs_arr)):
                i, j = int(pairs_arr[k, 0]), int(pairs_arr[k, 1])
                self.sim_lookup[(i, j)] = float(sims_arr[k])
                self.sim_lookup[(j, i)] = float(sims_arr[k])
            logger.info(f"Loaded {len(pairs_arr)} similarity pairs")

        # Load pre-computed DINOv2 embeddings (optional, for pair mining)
        self.dino_embeddings = None
        if dino_embeddings_path and Path(dino_embeddings_path).exists():
            data = np.load(dino_embeddings_path)
            self.dino_embeddings = data["embeddings"]

        # Mine pairs
        self.pairs = self._mine_pairs()

    def _mine_pairs(self) -> list:
        """Mine contrastive pairs based on dynamics and visual similarity."""
        n = len(self.metadata)
        pairs = []

        n_hard_neg = int(self.num_pairs * self.hard_neg_ratio)
        n_hard_pos = int(self.num_pairs * self.hard_pos_ratio)
        n_random = self.num_pairs - n_hard_neg - n_hard_pos

        group_keys = list(self._group_index.keys())

        # Hard negatives: SAME image, DIFFERENT physics → same visual, different dynamics
        for _ in range(n_hard_neg):
            grp = self.rng.choice(group_keys)
            members = self._group_index[grp]
            if len(members) >= 2:
                i, j = self.rng.choice(members, size=2, replace=False)
            else:
                i, j = self.rng.integers(0, n, size=2)
            dyn_sim = self._get_dynamics_similarity(i, j)
            pairs.append((int(i), int(j), dyn_sim))

        # Hard positives: DIFFERENT image, SIMILAR physics
        for _ in range(n_hard_pos):
            i = int(self.rng.integers(0, n))
            mass_i = self.metadata[i]["mass"]
            fric_i = self.metadata[i]["static_friction"]
            rest_i = self.metadata[i]["restitution"]
            grp_i = self.metadata[i].get("image_group", i)
            # Find candidates with similar physics but different image
            best_j, best_dist = int(self.rng.integers(0, n)), float('inf')
            for _ in range(20):  # sample 20 candidates
                j = int(self.rng.integers(0, n))
                grp_j = self.metadata[j].get("image_group", j)
                if grp_j == grp_i:
                    continue
                dist = abs(np.log(self.metadata[j]["mass"] + 1e-6) - np.log(mass_i + 1e-6)) + \
                       abs(self.metadata[j]["static_friction"] - fric_i) + \
                       abs(self.metadata[j]["restitution"] - rest_i)
                if dist < best_dist:
                    best_j, best_dist = j, dist
            dyn_sim = self._get_dynamics_similarity(i, best_j)
            pairs.append((i, best_j, dyn_sim))

        # Random pairs
        for _ in range(n_random):
            i, j = self.rng.integers(0, n, size=2)
            dyn_sim = self._get_dynamics_similarity(int(i), int(j))
            pairs.append((int(i), int(j), dyn_sim))

        return pairs

    def _get_dynamics_similarity(self, i: int, j: int) -> float:
        """Get dynamics similarity between two data points."""
        if (i, j) in self.sim_lookup:
            return self.sim_lookup[(i, j)]
        return self._compute_proxy_similarity(i, j)

    def _compute_proxy_similarity(self, i: int, j: int) -> float:
        """Compute dynamics similarity from physics trajectories on the fly.

        Uses the analytical physics engine to generate trajectories for both
        entries and computes L2-based similarity. This replaces the old
        parameter-distance proxy with actual trajectory similarity.
        """
        from dynaclip.data.generation import (
            PhysicsConfig, AnalyticalPhysicsEngine, DIAGNOSTIC_ACTIONS,
            compute_dynamics_similarity_l2,
        )
        m_i, m_j = self.metadata[i], self.metadata[j]
        pi = PhysicsConfig(
            mass=m_i["mass"],
            static_friction=m_i["static_friction"],
            restitution=m_i["restitution"],
            material=m_i.get("material", "default"),
        )
        pj = PhysicsConfig(
            mass=m_j["mass"],
            static_friction=m_j["static_friction"],
            restitution=m_j["restitution"],
            material=m_j.get("material", "default"),
        )
        engine = AnalyticalPhysicsEngine()
        traj_i = np.concatenate([engine.execute_diagnostic_action(a, pi) for a in DIAGNOSTIC_ACTIONS])
        traj_j = np.concatenate([engine.execute_diagnostic_action(a, pj) for a in DIAGNOSTIC_ACTIONS])
        return compute_dynamics_similarity_l2(traj_i, traj_j)

    def __len__(self) -> int:
        return len(self.pairs)

    def __getitem__(self, idx: int) -> dict:
        i, j, dyn_sim = self.pairs[idx]
        img_i = self._load_image(i)
        img_j = self._load_image(j)

        # Physics vectors: [log(mass), friction, restitution] for RnC loss
        mi, mj = self.metadata[i], self.metadata[j]
        physics_i = torch.tensor([
            np.log(mi["mass"] + 1e-6),
            mi["static_friction"],
            mi["restitution"],
        ], dtype=torch.float32)
        physics_j = torch.tensor([
            np.log(mj["mass"] + 1e-6),
            mj["static_friction"],
            mj["restitution"],
        ], dtype=torch.float32)

        return {
            "img_i": img_i,
            "img_j": img_j,
            "dynamics_similarity": torch.tensor(dyn_sim, dtype=torch.float32),
            "physics_i": physics_i,
            "physics_j": physics_j,
            "mass_i": torch.tensor(mi["mass"], dtype=torch.float32),
            "mass_j": torch.tensor(mj["mass"], dtype=torch.float32),
            "friction_i": torch.tensor(mi["static_friction"], dtype=torch.float32),
            "friction_j": torch.tensor(mj["static_friction"], dtype=torch.float32),
            "restitution_i": torch.tensor(mi["restitution"], dtype=torch.float32),
            "restitution_j": torch.tensor(mj["restitution"], dtype=torch.float32),
            "idx_i": i,
            "idx_j": j,
        }

    def _load_image(self, idx: int) -> torch.Tensor:
        """Load a REAL image from disk. Returns black image on failure."""
        img_path_str = self.metadata[idx]["image_path"]
        img_path = Path(img_path_str)

        # If relative, resolve against data_dir
        if not img_path.is_absolute():
            img_path = self.data_dir / img_path

        try:
            from PIL import Image
            img = Image.open(img_path).convert("RGB")
            return self.transform(img)
        except Exception:
            return torch.zeros(3, 224, 224)


# ---------------------------------------------------------------------------
# Invisible Physics test dataset
# ---------------------------------------------------------------------------
class InvisiblePhysicsDataset(Dataset):
    """Test pairs: same image, different physics properties.

    The key insight: both images look IDENTICAL to a standard vision model,
    but DynaCLIP should produce different embeddings because it has learned
    to encode physics-dependent representations.

    We load the SAME real image for both views but provide different physics labels.
    """

    def __init__(
        self,
        data_dir: str,
        test_file: str = "invisible_physics_test.json",
        transform: Optional[T.Compose] = None,
    ):
        super().__init__()
        self.data_dir = Path(data_dir)
        self.transform = transform or get_eval_transform()

        test_path = self.data_dir / test_file
        if not test_path.exists():
            raise FileNotFoundError(
                f"Invisible physics test file not found: {test_path}. "
                "Run data generation first."
            )
        with open(test_path) as f:
            self.pairs = json.load(f)
        logger.info(f"Loaded {len(self.pairs)} invisible physics test pairs")

    def __len__(self) -> int:
        return len(self.pairs)

    def __getitem__(self, idx: int) -> dict:
        pair = self.pairs[idx]
        img_path = Path(pair["image_path"])
        if not img_path.is_absolute():
            img_path = self.data_dir / img_path
        if not img_path.exists():
            raise FileNotFoundError(f"Test image not found: {img_path}")

        from PIL import Image
        img = Image.open(img_path).convert("RGB")
        img_tensor = self.transform(img)

        pa = pair["physics_a"]
        pb = pair["physics_b"]
        return {
            "img_a": img_tensor,      # Same image for both
            "img_b": img_tensor,      # Visually identical
            "mass_a": torch.tensor(pa["mass"], dtype=torch.float32),
            "mass_b": torch.tensor(pb["mass"], dtype=torch.float32),
            "friction_a": torch.tensor(pa["static_friction"], dtype=torch.float32),
            "friction_b": torch.tensor(pb["static_friction"], dtype=torch.float32),
            "restitution_a": torch.tensor(pa["restitution"], dtype=torch.float32),
            "restitution_b": torch.tensor(pb["restitution"], dtype=torch.float32),
            "heavier_label": torch.tensor(
                1 if pa["mass"] > pb["mass"] else 0, dtype=torch.long,
            ),
            "dynamics_similarity": torch.tensor(
                pair.get("dynamics_similarity", 0.5), dtype=torch.float32,
            ),
        }


# ---------------------------------------------------------------------------
# Linear probing dataset
# ---------------------------------------------------------------------------
class PhysicsProbeDataset(Dataset):
    """Dataset for linear probing: predict physics properties from frozen features."""

    def __init__(
        self,
        data_dir: str,
        split: str = "train",
        transform: Optional[T.Compose] = None,
        seed: int = 42,
    ):
        super().__init__()
        self.data_dir = Path(data_dir)
        self.transform = transform or get_eval_transform()
        self.rng = np.random.default_rng(seed)

        meta_path = self.data_dir / "metadata.json"
        if not meta_path.exists():
            raise FileNotFoundError(
                f"metadata.json not found at {meta_path}. "
                "Run data generation first."
            )
        with open(meta_path) as f:
            all_meta = json.load(f)

        # Train/val/test split: 70/15/15
        n = len(all_meta)
        idx = self.rng.permutation(n)
        if split == "train":
            sel = idx[:int(0.7 * n)]
        elif split == "val":
            sel = idx[int(0.7 * n):int(0.85 * n)]
        else:
            sel = idx[int(0.85 * n):]
        self.metadata = [all_meta[i] for i in sel]
        logger.info(f"PhysicsProbeDataset({split}): {len(self.metadata)} entries")

    def __len__(self) -> int:
        return len(self.metadata)

    def __getitem__(self, idx: int) -> dict:
        entry = self.metadata[idx]
        img_path = Path(entry["image_path"])
        if not img_path.is_absolute():
            img_path = self.data_dir / img_path

        try:
            from PIL import Image
            img = Image.open(img_path).convert("RGB")
            img_tensor = self.transform(img)
        except Exception:
            # Return a black image for corrupted/unreadable files
            img_tensor = torch.zeros(3, 224, 224)

        return {
            "image": img_tensor,
            "mass": torch.tensor(entry["mass"], dtype=torch.float32),
            "static_friction": torch.tensor(entry["static_friction"], dtype=torch.float32),
            "restitution": torch.tensor(entry["restitution"], dtype=torch.float32),
            "category": entry.get("category", "unknown"),
        }


# ---------------------------------------------------------------------------
# DataLoader creation utilities
# ---------------------------------------------------------------------------
def create_contrastive_dataloader(
    data_dir: str,
    batch_size: int = 1024,
    num_workers: int = 8,
    distributed: bool = False,
    **kwargs,
) -> DataLoader:
    dataset = DynaCLIPContrastiveDataset(data_dir=data_dir, **kwargs)
    sampler = DistributedSampler(dataset) if distributed else None
    return DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=(sampler is None),
        sampler=sampler,
        num_workers=num_workers,
        pin_memory=True,
        drop_last=True,
        persistent_workers=(num_workers > 0),
    )


def create_probe_dataloader(
    data_dir: str,
    split: str = "train",
    batch_size: int = 256,
    num_workers: int = 4,
) -> DataLoader:
    dataset = PhysicsProbeDataset(data_dir=data_dir, split=split)
    return DataLoader(
        dataset,
        batch_size=batch_size,
        shuffle=(split == "train"),
        num_workers=num_workers,
        pin_memory=True,
    )
